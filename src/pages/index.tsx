import { useState } from "react";
import type { MouseEventHandler } from "react";
import Head from "next/head";
import { Inter } from "next/font/google";
import { LazyImage } from "../../components/RandomFox";
import { random, uniqueId } from 'lodash'

const inter = Inter({ subsets: ["latin"] });

//generar a random function  1 a 123
const myRandom = () => random(1, 100)

// generate simple unique id 
const generateId = () => uniqueId()


export default function Home() {
  const [images, setImages] = useState<Array<IFoxImageItem>>([])

  const addNewFox: MouseEventHandler<HTMLButtonElement> = (event) => { // modo profesional de typescript para eventos de boton
    const newImageitem = {
      id: generateId(),
      url: `https://randomfox.ca/images/${myRandom()}.jpg`
    }
    setImages([...images, newImageitem])
    window.plausible("Signup")
  }
  
  return (
    <>
      <Head>
        <title>Curso de React con TypeScript</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
        <script
          defer
          data-domain="yourdomain.com"
          src="https://plausible.io/js/plausible.js"
        ></script>
      </Head>
      <main className="font-sans flex flex-col items-center justify-center relative ">
        <h3 className="text-3xl font-bold col-auto p-5">
        Curso de React con TypeScript
        </h3>
        <h1>Componente Lazy Image</h1>
        <p>Un componente genérico de React para cargar imágenes con lazy loading</p>
        <p>✨✨</p>
        <p>Las imágenes agregadas no se descargaran hasta que sean visibles en la pantalla</p>
        <p>✨✨</p>

        <button className="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded" onClick={addNewFox} >Add new fox</button>
        {images.map(({id, url}, index) => (
          <div key={id} className="p-4">
            <LazyImage 
              alt={'Fox'} 
              src={url} 
              title="Random Fox" 
              width={320} 
              height="auto"
              className="rounded bg-gray-300"
              onClick={() => console.log('hola')} 
              onLazyLoad={(img) => {
                console.log(`Image #${index + 1} cargada. Nodo: `, img)
              }
              }
            />
          </div>
        ))}
      </main>
    </>
  );
}
